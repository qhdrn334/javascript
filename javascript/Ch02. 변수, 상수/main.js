/*변수
값을 담는 그릇이다
변수에 담긴 값은 언제든 바꿀수 있다
변수를  선언할때는 let 이라는 키워드, 식별자,  값으로 나뉜다
같은 영역안에 같은 식별자를 가진 변수는 추가 생성이 불가능하다 */ 


let name = "홍길동"; /* 홍길동이라는 변수를 선언*/

let name = "방윤정";

console.log(name);

// 상수 - 변수와 마찬가지로 값을 담지만 할당된 값을 바꿀수 없다
// 상수 선언할떄는  const라는 키워드를 사용한다

const today = "화요일";
console.log(today);